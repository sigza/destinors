				<h3>COMO CHEGAR<br><br>
				Cambará do Sul fica a 195 quilômetros de Porto Alegre, chegar até lá de carro, você precisa seguir pela BR-290, conhecida como Freeway e, depois, tomar a RS-020 em direção a São José dos Ausentes.A rodovia estadual não está duplicada, mas está bem conservada.<br><BR>
				Esse é o caminho mais curto, mas há outra opção: seguindo pela BR-290 até Osório Filho e, depois, pegar a BR-101 em direção ao litoral. Nesse trajeto, a viagem fica cerca de 40 quilômetros mais longa.<br><BR>
				De ônibus, saindo de Porto Alegre a viagem dura cerca seis horas e, normalmente, o ônibus faz uma parada em São Francisco de Paula e, em alguns casos, há troca de veículo. Saindo de Caxias do Sul, a viagem dura quase três horas.
