				<h3>COMO CHEGAR<br><br>
				Para pessoas de estados que não sejam o Rio Grande do Sul, viajar de avião até Porto Alegre traz conforto e rapidez. Uma combinação de meios de transporte é o mais comum para se chegar a Gramado: avião+carro ou avião+ônibus.O aeroporto mais próximo de Gramado é o de Caxias do Sul, mas o aeroporto com melhor estrutura e o que a maioria das pessoas utiliza para chegar a Gramado é o de Porto Alegre, a cerca de 115 km de distância. A partir do aeroporto, você pode seguir para Gramado em um carro alugado, ônibus ou transporte de empresa turística.<br><BR>
