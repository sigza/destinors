				<h3>COMO CHEGAR<br><br>
				Para chegar em Garibaldi é simples, basta aterrissar no aeroporto de Porto Alegre ou de Caxias do Sul e seguir de carro ou ônibus em direção a cidade. A viagem será de um pouco mais de 100 quilômetros saindo de Porto Alegre e de 45 quilômetros partindo de Caxias e você terá muitas paisagens para contemplar no caminho.<br><BR>
				Ao chegar em Garibaldi você encontrará uma típica cidade do interior: tranquila, zero trânsito e recepção bastante hospitaleira. Durante as férias e em feriadões isso muda um pouco já que o fluxo de turistas deixa o local mais agitado.<br><BR>
				O principal atrativo de Garibaldi certamente são as vinícolas e sua internacionalmente famosa produção de espumantes.<H3><br><br><br><br><br>
				
